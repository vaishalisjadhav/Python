#Program1- Numeric Data Type
a=10
b=20
print(a+b)

c=10   #Integer 
d=10.5   #Float
e=complex(8+2)   #Complex

print("Type of c: ", type(c))
print("Type of d: ", type(d))
print("Type of e: ", type(e))

#Program2- String Data Type
name1="Vaishali"
name2="Payal"
print(name1+name2)
print("Type of name1: ", type(name1))

#Program3- Boolean Data Type(True/False)
x=True
y=False
print(x)
print(y)
print("Type of x: ", type(x))
print("Type of y: ", type(y))

#Program4- Sequence Data Type(List, Tuple)
list=["Vaishali", "Payal", [5,10,15]]
print(list)
print("Type of list: ", type(list))

tuple=(("Ram, Shyaam"), ("Shreya", "Shiv"))
print(tuple)
print("Type of tuple: ", type(tuple))

#Program5- Mapped Data Type (Dictionary)
dict={"name" : "Vaishu",
      "age" : 21,
      "Roll No": 57}
print(dict)
print("Type of dictionary: ", type(dict))